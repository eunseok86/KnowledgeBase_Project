package entity;

public class KkmResult {
	private String SmplStr2;
	private String Exp;
	
	
	public KkmResult() {
		super();
	}

	public KkmResult(String smplStr2, String exp) {
		super();
		SmplStr2 = smplStr2;
		Exp = exp;
	}

	public String getSmplStr2() {
		return SmplStr2;
	}

	public void setSmplStr2(String smplStr2) {
		SmplStr2 = smplStr2;
	}

	public String getExp() {
		return Exp;
	}

	public void setExp(String exp) {
		Exp = exp;
	}
	
	
}
